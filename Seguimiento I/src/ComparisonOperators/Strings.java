package ComparisonOperators;

public class Strings {
    public static void main(String[] args) {
        String s1 = "Hello";
        String s2 = "Hello";
        String s3 = "Goodbye";
        System.out.println(s1 == s2);
        System.out.println(s1 != s2);
        System.out.println(s1 == s3);
        System.out.println(s1 != s3);
    }
}
